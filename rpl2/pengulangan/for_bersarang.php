<?php

// perulangan untuk baris
for ($i=1; $i <= 5; $i++){
    //perulangan cetak bintang
    for ($a=1; $a <= $i; $a++){
        echo "*";
    }
    echo "<br>";
}
// perulangan untuk baris
for ($i=5; $i >= 1; $i--){
    //perulangan cetak bintang
    for ($a=5; $a >= $i; $a--){
        echo "*";
    }
    echo "<br>";
}

?>